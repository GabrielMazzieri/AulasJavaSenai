package aula02.exemplos;

public class Exemplo2 {
    public static void main(String[] args) {
        // Declarando variáveis
        float total, num1 = 2, num2 = 4;

        // Calcular soma: +
        total = num1 + num2;
        System.out.println("A soma é " + total);

        // Calcular soma: -
        total = num1 - num2;
        System.out.println("A subtração é " + total);

        // Calcular soma: *
        total = num1 * num2;
        System.out.println("A multiplicação é " + total);

        // Calcular soma: /
        total = num1/num2;
        System.out.println("A divisão é " + total);

        // Calcular soma: %
        total = num1%num2;
        System.out.println("O resto da divisão é " + total);
    }
}
