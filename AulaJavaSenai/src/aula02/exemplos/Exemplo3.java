package aula02.exemplos;

public class Exemplo3 {
    public static void main(String[] args) {
        // Declarando variÃ¡veis
        int num1 = 8, num2 = 7;
        boolean resposta;

        // Comparando maior: >
        resposta = num1 > num2;
        System.out.println(num1 + " > " + num2 + " = " + resposta);

        // Comparando menor: >
        resposta = num1 < num2;
        System.out.println(num1 + " < " + num2 + " = " + resposta);

        // Comparando maior ou igual: >
        resposta = num1 >= num2;
        System.out.println(num1 + " >= " + num2 + " = " + resposta);

        // Comparando menor ou igual: >
        resposta = num1 <= num2;
        System.out.println(num1 + " <= " + num2 + " = " + resposta);

        // Comparando igual: >
        resposta = num1 == num2;
        System.out.println(num1 + " == " + num2 + " = " + resposta);

        // Comparando diferente: >
        resposta = num1 != num2;
        System.out.println(num1 + " != " + num2 + " = " + resposta);
    }
}
